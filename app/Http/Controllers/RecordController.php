    {
        $records = Record::select('title', 'image', 'description')->get();

        for ($i=0; $i < count($records); $i++)
            $records[$i]->image = base64_encode(Storage::disk('local')->get("images\\records\\{$records[$i]->image}"));

        return $records;
    }

